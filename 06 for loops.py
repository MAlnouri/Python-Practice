'''
for x in range(start, stop, step)
start default = 0
step default = 1

start value is inclusive
stop value in exclusive
'''

# runs 10 times
for x in range(0, 10):
    print(x)

# runs 5 times
for x in range(5):
    print(x)

# 3 times increments in steps of 5
for x in range(0, 16, 5):
    print(x)